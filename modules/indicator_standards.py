    def validate_indicator_value(self, indicator_name: str, value: Any, risk_type: str = None) -> bool:
        """
        验证指标值是否符合规范
        
        Args:
            indicator_name: 指标名称
            value: 指标值
            risk_type: 风险类型（用于特殊验证，如崩塌类型的物质类型）
            
        Returns:
            是否符合规范
        """
        if indicator_name not in self.standards:
            return False
        
        standard = self.standards[indicator_name]
        allowed_values = standard.get("取值规则", [])
        
        # 特殊处理：崩塌类型的物质类型只允许"岩质"和"土岩混合"
        if indicator_name == "物质类型" and risk_type == "崩塌":
            allowed_values = ["岩质", "土岩混合"]
        
        # 如果是多选字段，需要检查每个值
        if standard.get("多选", False):
            if isinstance(value, str):
                # 支持分号和逗号分隔（兼容旧格式）
                if ";" in value:
                    values = [v.strip() for v in value.split(";")]
                elif "," in value:
                    values = [v.strip() for v in value.split(",")]
                else:
                    values = [value.strip()]
            elif isinstance(value, list):
                values = [str(v).strip() for v in value]
            else:
                values = [str(value).strip()]
            
            # 过滤空值
            values = [v for v in values if v]
            
            # 如果过滤后为空，返回False（不允许空值）
            if not values:
                return False
            
            for v in values:
                # 首先尝试精确匹配
                if v in allowed_values:
                    continue
                
                # 如果精确匹配失败，尝试匹配括号前的部分（兼容AI返回不带括号的值）
                matched = False
                for allowed_value in allowed_values:
                    allowed_base = allowed_value.split("（")[0].split("(")[0].strip()
                    if v == allowed_base:
                        matched = True
                        break
                
                if not matched:
                    return False
            return True
        else:
            # 单选字段：必须是指定值之一，不允许"未知"或None
            if value is None:
                return False
            
            value_str = str(value).strip()
            
            # 首先尝试精确匹配
            if value_str in allowed_values:
                return True
            
            # 如果精确匹配失败，尝试匹配括号前的部分（兼容AI返回不带括号的值）
            # 例如："陡" 应该匹配 "陡（＞30°）"
            for allowed_value in allowed_values:
                # 提取括号前的部分
                allowed_base = allowed_value.split("（")[0].split("(")[0].strip()
                if value_str == allowed_base:
                    return True
            
            return False


